package com.linebot.entry;

public class TrainDelay {
	String name;
	String company;
	String lastupdate_gmt;
	String source;

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getLastupdate_gmt() {
		return lastupdate_gmt;
	}

	public void setLastupdate_gmt(String lastupdate_gmt) {
		this.lastupdate_gmt = lastupdate_gmt;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getName() {
		return name;
	}
	
}
